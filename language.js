let currentLang = 'en'; // Default language
function toggleLanguage() {
    currentLang = (currentLang === 'en') ? 'ne' : 'en';
    // Update button text
    let htmlString = (currentLang === 'en') ? translations.ne.flag : translations.en.flag;
    // Create a new DOMParser instance
    const parser = new DOMParser();
    // Parse the string into a new Document object
    const doc = parser.parseFromString(htmlString, 'text/html');
    const langBtn = document.getElementById('lang-btn');
    langBtn.innerHTML = "";
    langBtn.appendChild(doc.body);

    // Update all text elements on the page
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });

    // Refresh the cart and products to reflect language change
    renderProducts(currentCategory);
    renderCart();
}
